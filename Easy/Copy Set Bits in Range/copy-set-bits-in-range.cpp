class Solution{
    public:
    int setSetBit(int x, int y, int l, int r){
        // code here
          int c=1;
        int mask=1;
        while(y!=0){
            if(((y& 1)==1)  && c>=l && c<=r){
                x=x | mask;
            }
            mask=mask<<1;
            y=y>>1;
            c++;
        }
        return x;
    }
};

